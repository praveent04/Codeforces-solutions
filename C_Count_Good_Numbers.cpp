ll check(ll n){
    ll ans=0;
    for(ll i=1;i<=n;i++){
        if(i%2 !=0 && i%3 !=0 && i%5 !=0 && i%7 !=0) ans++;
    }
    return ans;
}
void Solve(){
    ll l,r;
    cin>>l>>r;
    ll a = check(210);

    ll k1 = r/210;
    ll rem1 = r%210;
    ll k2 = (l-1)/210;
    ll rem2 = (l-1)%210;

    ll x = k1*a + check(rem1);
    ll y = k2*a + check(rem2);
    cout<<x-y<<endl;
}

