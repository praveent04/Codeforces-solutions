int s(vector<int>& c, int x, int n,int i,int h){

    if(x==0)return h;
    if(i==n-1){
        return x=c[i] ? h+1:0 ;
    }
    if(x<0)return 0;

    int take = 0;
    if(c[i]<=x){
        take =  s(c,x-c[i],n,i,h+1);
    }
    int ntake = s(c,x,n,i+1,h);
    return min(take,ntake);
}

void Solve(){
    int n,x;
    cin>>n>>x;
    vector<int> c(n);
    cin>>c;
    sort(all(c));
    reverse(all(c));

    int cnt = s(c,x,n,0,0);
    cout<<cnt<<endl;
}

