int n;
int arr[100001];
int dp[100001];
int rec(int i){
    if(i==n-1)return 0;
    int ans =INT_MAX;
    if(dp[i] !=-1)return dp[i];
    if(i+1<n){
        ans =min(ans,abs(arr[i] -arr[i+1]) + rec(i+1)); 
    }
    if(i+2<n){
        ans = min(ans, abs(arr[i] - arr[i+2] )+ rec(i+2));
    }
    return dp[i] = ans;
}
